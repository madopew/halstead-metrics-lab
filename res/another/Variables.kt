    // Mutable variables can be changed according to their types.
    var name = "HalilU"
    var isAwesome = true

    println( "Is " + name + " awesome? The answer is : " + isAwesome )

    var a = 3
    var b = 6
    println(a + b)

    val doubleNum = 123.45 // 64 bit number
    val floatNum  = 123.45f // 32 bit number
    val longNum = 123131313131L // 64 bit number

    val aDouble = a.toDouble()

