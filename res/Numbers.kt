fun main(args: Array<String>) {

    // one integer, the other one is decimal

    val a = 12
    val b = 25.3

    // +  -  /  *

    println(b + a )
    println(b - a)
    println(b / a)
    println(b * a)

    // two integer variables

    val a1 = 12
    val b1 = 25

    println(b1 + a1 )
    println(b1 - a1)
    println(b1 / a1)
    println(b1 * a1)
    println(b1 % a1)




}